    String sql_movie = 
        "SELECT r2.Movie_Name, r2.Theater_Name, r2.Movie_Date, r2.StartTime, r2.EndTime, r2.Movie_Count, r2.Room_No, r.Adult , r.Teen, r.Other\n" +
        "FROM Reservation r, reservationview r2\n" +
        "WHERE User_ID = '%s'\n" +
        "AND r.Time_ID = r2.Time_ID;";
    
    String sql_review = 
        "SELECT md.Movie_Name, mr.Review_Rating, mr.Review_Detail \n" +
        "FROM Movie_Review mr, Movie_Detail md \n" +
        "WHERE mr.User_ID = '%s'\n" +
        "AND mr.Movie_ID = md.Movie_ID;";

