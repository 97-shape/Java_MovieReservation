    public MyPageFrame(DBManager dbManager, String user_id) {
        this.dbManager = dbManager;
        this.user_id = user_id;
        initComponents();
        
        userLabel.setText(user_id);
        
        String colNames1[] = {"영화명", "평점", "리뷰"};
        String colNames2[] = {"영화명", "극장", "상영일"};
        modelReview = new DefaultTableModel(colNames1, 0);
        modelMovie = new DefaultTableModel(colNames2, 0);
        reviewTable.setModel(modelReview);
        movieTable.setModel(modelMovie);
        
        movies = dbManager.executeQuery(String.format(sql_movie, user_id));
        reviews = dbManager.executeQuery(String.format(sql_review, user_id));
        for (String[] movie : movies){
            modelMovie.addRow(movie);
        }
        for (String[] review : reviews){
            modelReview.addRow(review);
        }
        
        movieTable.addMouseListener(new java.awt.event.MouseAdapter() {
        @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int idx = movieTable.getSelectedRow();
                // 다음 프레임으로 값 전송
                new Complete_Page(movies.get(idx), dbManager, user_id).setVisible(true);
                dispose();
            }
        });
    }

